#!/usr/bin/env python3
"""Expand N3 grammar example files to 10 examples per point.

Workflow:
- keep existing curated examples
- append lesson-themed Japanese examples authored for this repo
- generate `translation` and `translationEn` in batches via public
  Google Translate web endpoint for faster bootstrapping
- stop when every N3 grammar point reaches 10 examples
"""

from __future__ import annotations

import json
import ssl
import sys
import urllib.parse
import urllib.request
from collections import defaultdict
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
EXAMPLE_ROOT = ROOT / 'assets' / 'data' / 'content' / 'grammar_examples' / 'n3'
REPORT_PATH = ROOT / 'docs' / 'reports' / 'n3-grammar-example-expansion-report.json'
SEP = 'ZXQSEPZXQ'
BATCH_SIZE = 20
SSL_CONTEXT = ssl._create_unverified_context()


def _read_json(path: Path):
    return json.loads(path.read_text(encoding='utf-8-sig'))


def _write_json(path: Path, payload) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(
        json.dumps(payload, ensure_ascii=False, indent=2) + '\n',
        encoding='utf-8',
    )


def _parse_expansions(raw: str) -> dict[int, dict[str, list[str]]]:
    payload: dict[int, dict[str, list[str]]] = defaultdict(lambda: defaultdict(list))
    for line in raw.splitlines():
        line = line.strip()
        if not line or line.startswith('#'):
            continue
        lesson_s, grammar_point, sentence = line.split('|', 2)
        payload[int(lesson_s)][grammar_point].append(sentence)
    return {lesson: dict(points) for lesson, points in payload.items()}


def _chunks(items: list[str], size: int):
    for index in range(0, len(items), size):
        yield items[index:index + size]


def _translate_request(text: str, target: str) -> str:
    query = urllib.parse.urlencode(
        {
            'client': 'gtx',
            'sl': 'ja',
            'tl': target,
            'dt': 't',
            'q': text,
        }
    )
    url = 'https://translate.googleapis.com/translate_a/single?' + query
    raw = urllib.request.urlopen(url, timeout=30, context=SSL_CONTEXT).read().decode('utf-8')
    data = json.loads(raw)
    return ''.join(part[0] for part in data[0])


def _translate_batch(sentences: list[str], target: str) -> list[str]:
    translated: list[str] = []
    for chunk in _chunks(sentences, BATCH_SIZE):
        joined = ('\n' + SEP + '\n').join(chunk)
        output = _translate_request(joined, target)
        parts = [part.strip() for part in output.split(SEP)]
        if len(parts) != len(chunk):
            parts = [_translate_request(sentence, target).strip() for sentence in chunk]
        translated.extend(parts)
    return translated


def main() -> int:
    if hasattr(sys.stdout, 'reconfigure'):
        sys.stdout.reconfigure(encoding='utf-8')

    expansions = _parse_expansions(RAW_SENTENCES)
    report = {
        'lessons': [],
        'totalAdded': 0,
    }

    for lesson_id in range(51, 76):
        path = EXAMPLE_ROOT / f'lesson_{lesson_id}.json'
        data = _read_json(path)
        block_map = {item['grammarPoint']: item for item in data}
        lesson_added = 0

        for grammar_point, new_sentences in expansions[lesson_id].items():
            if grammar_point not in block_map:
                raise KeyError(f'Lesson {lesson_id} missing grammar point: {grammar_point}')

            block = block_map[grammar_point]
            examples = block.setdefault('examples', [])
            existing_sentences = {item.get('sentence', '').strip() for item in examples}
            filtered = [sentence for sentence in new_sentences if sentence not in existing_sentences]
            needed = 10 - len(examples)

            if needed <= 0:
                continue
            if len(filtered) < needed:
                raise ValueError(
                    f'Lesson {lesson_id} / {grammar_point} needs {needed} examples but only {len(filtered)} new sentences are available.'
                )

            additions = filtered[:needed]
            vi_list = _translate_batch(additions, 'vi')
            en_list = _translate_batch(additions, 'en')

            for sentence, translation_vi, translation_en in zip(additions, vi_list, en_list):
                examples.append(
                    {
                        'sentence': sentence,
                        'translation': translation_vi,
                        'translationEn': translation_en,
                    }
                )
                lesson_added += 1

        _write_json(path, data)
        report['lessons'].append(
            {
                'lessonId': lesson_id,
                'addedExamples': lesson_added,
                'path': str(path.relative_to(ROOT)).replace('\\', '/'),
            }
        )
        report['totalAdded'] += lesson_added

    _write_json(REPORT_PATH, report)
    print(json.dumps(report, ensure_ascii=False, indent=2))
    return 0


